//! Build script: compile every `.cu` file under
//! `kernels/sm_<CTOX_CUDA_SM>/` to PTX via `nvcc`, then generate an
//! `OUT_DIR/ptx_registry.rs` file that holds each PTX blob as a
//! `const &[u8]` via `include_bytes!`. Kernel wrappers in
//! `src/kernels/` then `include!` the registry and load the relevant
//! blob at first call.
//!
//! Gated on the `cuda` Cargo feature — when off this script is a
//! no-op so the crate still compiles on non-CUDA hosts (Mac dev,
//! CI without nvcc).
//!
//! Env overrides:
//!   CTOX_CUDA_SM=86        Target SM capability (default: 86 = A6000).
//!                          Selects `kernels/sm_<SM>/` as the compile
//!                          set. Only sm_86 is populated today; sm_80,
//!                          sm_89, sm_90 are scaffolded but empty.
//!                          TODO: multi-arch fatbin build + runtime
//!                          dispatch once all four dirs are populated.
//!   NVCC=/path/to/nvcc     Override nvcc binary location.

use std::env;
use std::fs;
use std::path::PathBuf;
use std::process::Command;

fn main() {
    let cuda_on = env::var("CARGO_FEATURE_CUDA").is_ok();
    let out_dir = PathBuf::from(env::var("OUT_DIR").expect("OUT_DIR not set"));
    let registry_path = out_dir.join("ptx_registry.rs");

    if !cuda_on {
        fs::write(
            &registry_path,
            "// AUTO-GENERATED: cuda feature is off, registry is empty.\n\
             pub struct PtxBlob { pub name: &'static str, pub bytes: &'static [u8] }\n\
             pub static PTX_BLOBS: &[PtxBlob] = &[];\n",
        )
        .expect("write empty ptx_registry");
        return;
    }

    let manifest_dir =
        PathBuf::from(env::var("CARGO_MANIFEST_DIR").expect("CARGO_MANIFEST_DIR not set"));

    let sm = env::var("CTOX_CUDA_SM").unwrap_or_else(|_| "86".to_string());
    let kernels_dir = manifest_dir.join("kernels").join(format!("sm_{}", sm));
    let ptx_dir = out_dir.join("ptx");
    fs::create_dir_all(&ptx_dir).expect("mkdir ptx");

    println!("cargo:rerun-if-changed=kernels");
    println!("cargo:rerun-if-env-changed=CTOX_CUDA_SM");
    println!("cargo:rerun-if-env-changed=NVCC");

    let nvcc = env::var("NVCC").unwrap_or_else(|_| "nvcc".to_string());

    // Collect .cu files from the selected SM directory.
    let mut cu_files: Vec<PathBuf> = Vec::new();
    if kernels_dir.is_dir() {
        for entry in fs::read_dir(&kernels_dir).expect("read kernels dir") {
            let entry = entry.expect("dir entry");
            let path = entry.path();
            if path.extension().and_then(|s| s.to_str()) == Some("cu") {
                println!("cargo:rerun-if-changed={}", path.display());
                cu_files.push(path);
            }
        }
    } else {
        println!(
            "cargo:warning=kernels/sm_{} not populated — CTOX_CUDA_SM={} yields empty PTX registry",
            sm, sm
        );
    }
    cu_files.sort();

    let mut compiled: Vec<(String, PathBuf)> = Vec::new();
    for cu in &cu_files {
        let stem = cu
            .file_stem()
            .and_then(|s| s.to_str())
            .expect("kernel stem");
        let ptx_out = ptx_dir.join(format!("{}.ptx", stem));

        // Include-path set for vendored upstream kernels.
        //
        // llama.cpp ggml-cuda kernels include each other and the ggml
        // root headers (ggml.h, ggml-impl.h, ggml-common.h) plus the
        // vendor-abstraction headers (vendors/cuda.h). We vendored the
        // minimum header forest into vendor/{ggml-cuda,ggml-include};
        // nvcc resolves everything via these -I flags without a
        // submodule or a bloated copy of the whole llama.cpp tree.
        let vendor_ggml_cuda = manifest_dir.join("vendor").join("ggml-cuda");
        let vendor_ggml_include = manifest_dir.join("vendor").join("ggml-include");

        let status = Command::new(&nvcc)
            .arg("--ptx")
            .arg("-arch")
            .arg(format!("sm_{}", sm))
            .arg("-O3")
            .arg("-std=c++17")
            .arg("--ptxas-options=-v")
            // Include paths must come BEFORE the input file so nvcc
            // resolves the vendor headers referenced from the kernel.
            .arg(format!("-I{}", vendor_ggml_cuda.display()))
            .arg(format!("-I{}", vendor_ggml_include.display()))
            .arg("-o")
            .arg(&ptx_out)
            .arg(cu)
            .status()
            .unwrap_or_else(|e| {
                panic!(
                    "nvcc not runnable ({}). Set NVCC=/path/to/nvcc or install CUDA \
                     toolkit. err={}",
                    &nvcc, e
                )
            });
        if !status.success() {
            panic!("nvcc failed compiling {}", cu.display());
        }

        compiled.push((stem.to_string(), ptx_out));
    }

    let mut out = String::new();
    out.push_str("// AUTO-GENERATED by build.rs — do not edit.\n");
    out.push_str("// Source: kernels/sm_<SM>/*.cu, compiled via nvcc → PTX, embedded via include_bytes!\n\n");
    out.push_str("pub struct PtxBlob {\n");
    out.push_str("    pub name: &'static str,\n");
    out.push_str("    pub bytes: &'static [u8],\n");
    out.push_str("}\n\n");

    for (name, path) in &compiled {
        let upper = name.to_uppercase();
        out.push_str(&format!(
            "pub const {}_PTX: &[u8] = include_bytes!(r\"{}\");\n",
            upper,
            path.display()
        ));
    }
    out.push('\n');

    out.push_str("pub static PTX_BLOBS: &[PtxBlob] = &[\n");
    for (name, _) in &compiled {
        let upper = name.to_uppercase();
        out.push_str(&format!(
            "    PtxBlob {{ name: \"{}\", bytes: {}_PTX }},\n",
            name, upper
        ));
    }
    out.push_str("];\n");

    fs::write(&registry_path, out).expect("write ptx_registry.rs");
}
